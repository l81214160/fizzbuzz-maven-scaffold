
public class Area {
	// 长
	private int i;
	// 宽
	private int j;

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public int getJ() {
		return j;
	}

	public void setJ(int j) {
		this.j = j;
	}

	public Area(int i, int j) {
		this.i = i;
		this.j = j;
	}
}
